// Music player class with basic controls
class MusicPlayer {
    public void play() {
        System.out.println("Music is now playing");
    }

    public void pause() {
        System.out.println("Music has been paused");
    }

    public void skip() {
        System.out.println("Skipping to the next track");
    }

    public void increaseVolume() {
        System.out.println("Turning the volume up");
    }

    public void decreaseVolume() {
        System.out.println("Turning the volume down");
    }
}
